import streamlit as st
import random

st.set_page_config(page_title="Rock, Paper, Scissors", page_icon="ğŸª¨ğŸ“„âœ‚ï¸")

st.title("ğŸª¨ğŸ“„âœ‚ï¸ Rock, Paper, Scissors")
st.markdown("Welcome to the classic game! Can you beat the computer?")

# Rules toggle
with st.expander("ğŸ“œ Show Game Rules"):
    st.markdown("""
    - **Rock** crushes **Scissors**
    - **Scissors** cuts **Paper**
    - **Paper** covers **Rock**
    """)

# Initialize session state
if "user_score" not in st.session_state:
    st.session_state.user_score = 0
    st.session_state.computer_score = 0
    st.session_state.ties = 0

# Game options
choices = ["Rock", "Paper", "Scissors"]
emojis = {"Rock": "ğŸª¨", "Paper": "ğŸ“„", "Scissors": "âœ‚ï¸"}

# Game logic
def determine_winner(user, computer):
    if user == computer:
        st.session_state.ties += 1
        return "ğŸ¤ It's a tie!"
    elif (user == "Rock" and computer == "Scissors") or \
         (user == "Paper" and computer == "Rock") or \
         (user == "Scissors" and computer == "Paper"):
        st.session_state.user_score += 1
        return "ğŸ‰ You win!"
    else:
        st.session_state.computer_score += 1
        return "ğŸ’» Computer wins!"

# User choice
user_choice = st.radio("Choose your move:", choices, horizontal=True, format_func=lambda x: f"{emojis[x]} {x}")

# Play the game
if st.button("Play"):
    computer_choice = random.choice(choices)
    st.markdown(f"**You chose:** {emojis[user_choice]} {user_choice}")
    st.markdown(f"**Computer chose:** {emojis[computer_choice]} {computer_choice}")

    result = determine_winner(user_choice, computer_choice)
    st.subheader(result)

    if "You win" in result:
        st.balloons()

# Display score
st.markdown("### ğŸ§® Scoreboard")
st.write(f"ğŸ‘¤ You: {st.session_state.user_score}")
